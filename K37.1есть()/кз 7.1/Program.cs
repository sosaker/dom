using System;  // Подключаем пространство имен System, которое предоставляет базовые классы и методы, такие как Console, String и другие.

namespace ConsoleApp  // Определяем пространство имен для нашего приложения, чтобы избежать конфликтов с другими программами.

{
    // Класс для преобразования строки в целое число
    public class StringToInt
    {
        // Статический метод для преобразования строки в число
        public static int Convert(string input)
        {
            return int.Parse(input);  // Преобразуем строку в число с помощью метода int.Parse()
        }
    }

    // Главный класс программы
    class Program
    {
        // Метод Main - точка входа в программу
        static void Main(string[] args)
        {
            Console.Write("введите число:");  // Просим пользователя ввести число, выводя сообщение в консоль.
            string number = Console.ReadLine();  // Читаем строку, введённую пользователем.

            Console.WriteLine();  // Печатаем пустую строку для оформления.

            try
            {
                // Пытаемся преобразовать строку в число с помощью метода Convert
                int convertedValue = StringToInt.Convert(number);
                // Если преобразование прошло успешно, выводим результат
                Console.WriteLine($"введённое число: {convertedValue}");
            }

            // Обработка исключения, если введённое число слишком большое или слишком маленькое для типа int
            catch (OverflowException ex)
            {
                // Если возникла ошибка переполнения, выводим сообщение
                Console.WriteLine($"число вне диапазона: {ex.Message}");
            }

            // Обработка исключения, если введённая строка не может быть преобразована в число
            catch (FormatException ex)
            {
                // Если введена не строка, представляющая число, выводим сообщение
                Console.WriteLine($"введено не число: {ex.Message}");
            }
        }
    }
}
