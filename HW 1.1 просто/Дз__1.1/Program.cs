using System;

class Program
{
    static void Main()
    {
        // Запрашиваем у пользователя число для нахождения всех простых чисел до этого числа
        Console.Write("Введите число: ");
        int n = int.Parse(Console.ReadLine()); // Читаем число и конвертируем его в тип int

        // Цикл для проверки всех чисел от 2 до n
        for (int i = 2; i <= n; i++)
        {
            bool isPrime = true; // Изначально предполагаем, что число простое

            // Вложенный цикл для проверки делимости числа i на все числа от 2 до sqrt(i)
            for (int j = 2; j * j <= i; j++)
            {
                if (i % j == 0) // Если i делится на j без остатка, то оно не простое
                {
                    isPrime = false; // Отмечаем, что число не простое
                    break; // Прерываем цикл, так как делитель найден
                }
            }

            // Если после проверки isPrime осталось истинным, значит число простое
            if (isPrime)
            {
                Console.WriteLine(i); // Выводим простое число
            }
        }
    }
}
